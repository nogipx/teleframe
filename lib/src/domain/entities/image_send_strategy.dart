/// Стратегия отправки изображений в сообщении
enum ImageSendStrategy {
  /// Автоматический выбор (по умолчанию):
  /// - 0 изображений: текстовое сообщение
  /// - 1 изображение: sendPhoto с caption и кнопками
  /// - 2+ изображений: sendMediaGroup + отдельное сообщение с текстом и кнопками
  auto,

  /// Всегда отправлять изображения отдельным сообщением:
  /// - Сначала sendPhoto или sendMediaGroup (без кнопок)
  /// - Затем текстовое сообщение с кнопками
  ///
  /// Полезно, когда текст длинный и не помещается в caption (лимит 1024 символа)
  separateMessage,

  /// Всегда пытаться объединить в одно сообщение:
  /// - 1 изображение: sendPhoto с caption и кнопками
  /// - 2+ изображений: sendMediaGroup + сообщение с текстом и кнопками
  /// - Если текст > 1024 символов, автоматически переключается на separateMessage
  combined,
}
